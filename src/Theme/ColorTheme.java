package Theme;

import java.awt.Color;

public class ColorTheme {
	public Color primary = new Color(50, 56, 117);
	public Color secondary = new Color(125, 130, 176);
	
	public ColorTheme() {}
	
	public Color getPrimaryColor() {
		return primary;
	}
	
	public Color getSecondaryColor() {
		return secondary;
	}
}
